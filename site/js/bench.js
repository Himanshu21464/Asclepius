// Asclepius — load real numbers into /benchmarks from assets/bench.json.
//
// The HTML ships hardcoded fallback numbers so the page reads correctly
// without JS or before fetch resolves. When this script runs, it patches
// any element marked `[data-bench]` with the corresponding key from the
// JSON. Keys are dotted paths into the JSON; the value is formatted by
// `data-bench-fmt`.
//
//   <span data-bench="end_to_end_inference.p99_us"
//         data-bench-fmt="us"></span>
//
// Formats:
//   us  → "147.6 µs"     (1-decimal microseconds, smart unit upgrade to ms/s)
//   ns  → "17.4 µs"      (ns input, format as µs/ns)
//   mb  → "613 MB/s"
//   ops → "57.3k ops/s"
//   raw → exact value (1 decimal)
//
// All formats round to two significant figures except for ops (k/M/G).

(() => {
    'use strict';

    function get(obj, path) {
        return path.split('.').reduce((o, k) => (o == null ? o : o[k]), obj);
    }

    function fmtUs(value, unitInputNs) {
        const us = unitInputNs ? value / 1000 : value;
        if (us >= 1e6) return (us / 1e6).toFixed(2) + ' s';
        if (us >= 1e3) return (us / 1e3).toFixed(2) + ' ms';
        if (us >= 10)  return us.toFixed(1) + ' µs';
        return us.toFixed(2) + ' µs';
    }

    function fmtMb(mbPerSec) {
        if (mbPerSec >= 1024) return (mbPerSec / 1024).toFixed(2) + ' GB/s';
        return Math.round(mbPerSec) + ' MB/s';
    }

    function fmtOps(ops) {
        if (ops >= 1e9) return (ops / 1e9).toFixed(1) + 'G ops/s';
        if (ops >= 1e6) return (ops / 1e6).toFixed(1) + 'M ops/s';
        if (ops >= 1e3) return (ops / 1e3).toFixed(1) + 'k ops/s';
        return Math.round(ops) + ' ops/s';
    }

    function format(value, fmt) {
        if (value == null || isNaN(value)) return '—';
        switch (fmt) {
            case 'us':  return fmtUs(value, false);
            case 'ns':  return fmtUs(value, true);
            case 'mb':  return fmtMb(value);
            case 'ops': return fmtOps(value);
            case 'raw': return Number(value).toFixed(1);
            default:    return String(value);
        }
    }

    async function load() {
        try {
            const res = await fetch('assets/bench.json', { cache: 'no-cache' });
            if (!res.ok) return;
            const json = await res.json();
            const targets = document.querySelectorAll('[data-bench]');
            let patched = 0;
            for (const el of targets) {
                const key = el.dataset.bench;
                const fmt = el.dataset.benchFmt || 'raw';
                const v = get(json, key);
                if (v == null || isNaN(Number(v))) continue;
                el.textContent = format(Number(v), fmt);
                el.classList.add('is-live');
                patched += 1;
            }
            // Update the methodology footer with the real sample size + version
            const meta = document.querySelector('[data-bench-meta]');
            if (meta && json.sample_size) {
                const ver = json.asclepius_version || '';
                meta.textContent =
                    `live · v${ver} · ${json.sample_size.toLocaleString()} samples · ` +
                    `regenerated by examples/04_bench`;
            }
        } catch (e) {
            // Fall back silently to the hardcoded numbers in HTML.
        }
    }

    if (document.readyState === 'loading') {
        document.addEventListener('DOMContentLoaded', load);
    } else {
        load();
    }
})();
